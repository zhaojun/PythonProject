def add_quotes_to_list(text):
    """
    给括号内的列表元素统一加上引号
    :param text: 原始文本（如 "[755小门,654D,701西,701北,701东,781,782钛合金,786南,786北,动力水暖,452#,570]"）
    :return: 每个元素带引号的新文本
    """
    # 1. 去除首尾的方括号
    content = text.strip('[]')
    # 2. 按逗号分割成单个元素
    elements = [elem.strip() for elem in content.split(',')]  # strip() 去除元素前后可能的空格
    # 3. 给每个元素加上双引号，再用逗号连接
    quoted_elements = [f'"{elem}"' for elem in elements]
    # 4. 重新加上方括号，返回结果
    result = '[' + ', '.join(quoted_elements) + ']'
    return result


